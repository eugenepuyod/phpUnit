<?php

namespace App;

Class Calculator {
    public function add($num1, $num2){
        return $num1 + $num2;
    }
    public function minus($num1, $num2){
        return $num1 - $num2;
    }
    public function devide($num1, $num2){
        return $num1 / $num2;
    }
    public function multiply($num1, $num2){
        return $num1 * $num2;
    }
}